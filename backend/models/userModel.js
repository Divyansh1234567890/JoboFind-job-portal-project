import mongoose from "mongoose";
const userSchema = mongoose.Schema({
  name:{
    type:String,
    required:true
  },
  email:{
    type:String,
    required:true,
    unique:true
  },
  password:{
    type:String,
    required:true,
  },
  role:{
    type:String,
    enum:["student","recruiter","admin"],
    default:"student"
  },
  image:{
    type:String,
    default:""
  },
  bio:{
    type:String,
    default:""
  },
  phone:{
    type:String,
    default:""
  },
  resume:{
    type:String,
    default:""
  },
  location:{
    type:String,
    default:""
  },
  education:{
    type:String,
    default:"",
  },
  experience:{
    type:String,
    default:"",
  },
  skills:{
    type:String,
    default:"",
  },
},
{
  timestamps:true
}
);
const User = mongoose.model('User',userSchema);
export default User;
